    /**
     * write file
     *
     * @param file   the file to be opened for writing.
     * @param stream the input stream
     * @param append if <code>true</code>, then bytes will be written to the end of the file rather than the beginning
     * @return return true
     * @throws RuntimeException if an error occurs while operator FileOutputStream
     */
    private static boolean writeFile(File file, FileInputStream stream, boolean append, OnProgressListener progressListener) {
        OutputStream o = null;
        try {
            makeDirs(file.getAbsolutePath());
            o = new FileOutputStream(file, append);
            byte data[] = new byte[2048];
            int length;
            long total = 0;
            if (progressListener != null) progressListener.onStart();
            while ((length = stream.read(data)) != -1) {
                total += length;
                o.write(data, 0, length);
                if (progressListener != null)
                    progressListener.onProgress((int) ((total * 100) / totalFileLength));
            }
            stream.close();
            o.flush();
            totalFileLength = -1;
            if (progressListener != null) progressListener.onComplete();
            return true;
        } catch (FileNotFoundException e) {
            if (progressListener != null) progressListener.onError();
            throw new RuntimeException("FileNotFoundException occurred. ", e);
        } catch (IOException e) {
            if (progressListener != null) progressListener.onError();
            throw new RuntimeException("IOException occurred. ", e);
        }
    }

